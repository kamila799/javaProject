package ifElse;

public class EvenOrOdd {
    public static void main(String[] args) {

        int number = 9;
        if (number % 2 == 0){
            System.out.println("even");
        }
        else {
            System.out.println("odd");
        }

        int number1 = 19;
        if (number1 % 2 == 0){
            System.out.println("Even!");
        }else{
            System.out.println("Odd!");
        }



    }
}
