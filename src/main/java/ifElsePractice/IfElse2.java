package ifElsePractice;

public class IfElse2 {
    public static void main(String[] args) {

        int n = 10;

        if (n > 10){
            System.out.println(" > 10 ");
        }
        else if (n == 10){
            System.out.println(" = 10 ");
        }
        else {
            System.out.println(" < 10 ");
        }



    }
}
